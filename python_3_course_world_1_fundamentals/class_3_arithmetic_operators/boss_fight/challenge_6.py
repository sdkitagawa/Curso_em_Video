# Create an algorithm that reads a number and shows its double, triple and square root

userNumber = int(input("Insert a number: "))
doubleNumber = userNumber * 2
tripleNumber = userNumber * 3
squareRoot = userNumber ** 0.5

print(f"Your number: {userNumber}\nThe double of your number: {doubleNumber}\nThe triple of your number: {tripleNumber}\nThe square root of your number: {squareRoot}")
